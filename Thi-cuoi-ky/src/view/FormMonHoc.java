    public void loadData()
    {
        try {
            File f = new File(file);
            if(f.exists())
                list = IOFile.doc(file);
            else
                IOFile.viet(file, list);
            show(list);
        } catch (Exception e) {
        }
    }
    
    public void show(List<MonHoc> list)
    {
        d.setRowCount(0);
        for(MonHoc i : list)
        {
            d.addRow(i.toObjects());
        }
    }
