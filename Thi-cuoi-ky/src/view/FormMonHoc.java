    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        int row = jTable1.getSelectedRow();
        if(row < 0 || row > jTable1.getRowCount() - 1)
        {
            JOptionPane.showMessageDialog(this, "Chon dong trong bang!");
        }
        else
        {
            buttonState(false);
            sua = true;
            if(them)
                them = false;
            jTextField2.requestFocus();
        }
    }//GEN-LAST:event_btSuaActionPerformed

    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        them = true;
        if(list.size() > 0)
        {
            MonHoc.setIds(list.get(list.size() - 1).getId() + 1);
        }
        jTextField1.setText(MonHoc.getIds() + "");
        jTextField2.requestFocus();
        buttonState(false);
        if(sua)
        {
            sua = false;
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        if(them)
        {
            them = false;
            try {
                int id = Integer.parseInt(jTextField1.getText());
                String ten = jTextField2.getText();
                int soTin = Integer.parseInt(jTextField3.getText());
                String loai = jComboBox1.getSelectedItem().toString();
                if(ten.isEmpty())
                    throw new TrongException();
                MonHoc temp = new MonHoc(id, ten, soTin, loai);
                d.addRow(temp.toObjects());
                list.add(temp);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Nhap vao so!");
                jTextField3.requestFocus();
            } catch (TrongException ex) {
                JOptionPane.showMessageDialog(this, "Khong de trong!");
                jTextField2.requestFocus();
            }
        }
        
        if(sua)
        {
            sua = false;
            int row = jTable1.getSelectedRow();
            try {
                int id = Integer.parseInt(jTextField1.getText());
                String ten = jTextField2.getText();
                int soTin = Integer.parseInt(jTextField3.getText());
                String loai = jComboBox1.getSelectedItem().toString();
                if(ten.isEmpty())
                    throw new TrongException();
                MonHoc temp = new MonHoc(id, ten, soTin, loai);
                list.set(row, temp);
                d.setValueAt(id, row, 0);
                d.setValueAt(ten, row, 1);
                d.setValueAt(soTin, row, 2);
                d.setValueAt(loai, row, 3);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Nhap vao so!");
                jTextField3.requestFocus();
            } catch (TrongException ex) {
                JOptionPane.showMessageDialog(this, "Khong de trong!");
                jTextField2.requestFocus();
            }
        }
        buttonState(true);
    }//GEN-LAST:event_btCapNhatActionPerformed

