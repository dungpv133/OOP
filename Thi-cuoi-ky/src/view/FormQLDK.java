    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        int maMH = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int maSV = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        if(them)
        {
            if(ktTrung(maMH, maSV))
            {
                    them = false;
                try {
                    
                    String ngay = jTextField3.getText();
//                    if(!ngay.matches("^\\d{2}\\-\\d{2}\\-\\d{4}"))
//                        throw new SoException();
                    if(ngay.isEmpty())
                        throw new TrongException();
                    QLDK temp = new QLDK(getMonHoc(maMH), getSinhVien(maSV), ngay);
                    d.addRow(temp.toObjects());
                    list.add(temp);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Nhap vao so!");
                } catch (TrongException ex) {
                    JOptionPane.showMessageDialog(this, "Khong de trong!");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Trung du lieu!");
            }
        }
        
        
        buttonState(true);
    }//GEN-LAST:event_btCapNhatActionPerformed

