    public void loadMa()
    {
        list1 = IOFile.doc(file1);
        list2 = IOFile.doc(file2);
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        for(MonHoc i : list1)
        {
            jComboBox1.addItem(i.getId() + "");
        }
        for(SinhVien i : list2)
        {
            jComboBox2.addItem(i.getId() + "");
        }
    }
