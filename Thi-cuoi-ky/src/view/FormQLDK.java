    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        if(jComboBox3.getSelectedIndex() == 0)
        {
            list.sort(new Comparator<QLDK>(){
                @Override
                public int compare(QLDK o1, QLDK o2) {
                    String t1 = o1.getMh().getTen();
                    String t2 = o2.getMh().getTen();
                    String name1 = t1.substring(t1.lastIndexOf(" ") + 1) + t1;
                    String name2 = t2.substring(t2.lastIndexOf(" ") + 1) + t2;
                    return name1.compareToIgnoreCase(name2);
                }
                
            });
            showList(list);
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed


