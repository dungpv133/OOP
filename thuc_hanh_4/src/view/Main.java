    private void btSuaCTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaCTActionPerformed
        int r = jTable1.getSelectedRow();
        if(r >= 0 && r < jTable1.getRowCount())
        {
            String hoten = txtTenCT.getText();
            String ma = txtMaCT.getText();
            String tuoi = txtTuoi.getText();
            String bang = cbBangCap.getSelectedItem().toString();
            String luong = txtLuong.getText();
            
            tmCT.setValueAt(ma, r, 0);
            tmCT.setValueAt(hoten, r, 1);
            tmCT.setValueAt(tuoi, r, 2);
            tmCT.setValueAt(bang, r, 3);
            tmCT.setValueAt(luong, r, 4);
            int ma1 = Integer.parseInt(txtMaCT.getText());
            long luong1 = Long.parseLong(luong);
            int tuoi1 = Integer.parseInt(tuoi);
            CauThu temp = new CauThu(ma1, tuoi1, hoten, bang, luong1);
            listCauThu.set(r, temp);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Chon cau thu de sua! ");
            return;
        }
        
    }//GEN-LAST:event_btSuaCTActionPerformed

