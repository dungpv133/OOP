    private void loadCT()
    {
        File f = new File(fCT);
        if(f.exists())
        {
            listCauThu = IOFile.doc(fCT);
        }
        else
        {
            listCauThu = new ArrayList<>();
        }
    }
    
    private void loadVT()
    {
        File f = new File(fVT);
        if(f.exists())
        {
            listViTri = IOFile.doc(fVT);
        }
        else
        {
            listViTri = new ArrayList<>();
        }
    }
    
    private void showCT(List <CauThu> lst)
    {
        tmCT.setRowCount(0);
        for(CauThu i : lst)
        {
            tmCT.addRow(i.toObject());
        }
    }
    
    private void showVT(List <ViTri> lst)
    {
        tmVT.setRowCount(0);
        for(ViTri i : lst)
        {
            tmVT.addRow(i.toObject());
        }
    }
