    private void btThemCTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemCTActionPerformed
        // TODO add your handling code here:
        int n = listCauThu.size();
        if(n > 0)
        {
            CauThu.setSma(listCauThu.get(n - 1).getMaCauThu()+ 1);
        }
        txtMaCT.setText(CauThu.getSma() + "");
        int maCauThu, tuoi;
        String hoTen, bangCap;
        long luong;
        try {
            hoTen = txtTenCT.getText();
            bangCap = cbBangCap.getSelectedItem().toString();
            tuoi = Integer.parseInt(txtTuoi.getText());
            luong = Long.parseLong(txtLuong.getText());
            CauThu cauThu = new CauThu(tuoi, hoTen, bangCap, luong);
            tmCT.addRow(cauThu.toObject());
            listCauThu.add(cauThu);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong! ");
            return;
        } catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Nhap vao so! ");
            return;
        }
    }//GEN-LAST:event_btThemCTActionPerformed

