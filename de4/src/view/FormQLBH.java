    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        int maMatHang = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int maNhanVien = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        if(them)
        {
            them = false;
            if(ktMax(maNhanVien))
            {
                if(ktTrung(maMatHang, maNhanVien))
                {
                    QLBH temp = new QLBH(getMatHang(maMatHang), 
                            getNhanVien(maNhanVien));
                    list.add(temp);
                    d.addRow(temp.toObjects());
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Mat hang va nhan vien"
                            + " da ton tai!");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Qua so luong hang nhan vien"
                        + " co the ban!");
            }
        }
        if(sua)
        {
            sua = false;
            int row = jTable1.getSelectedRow();
            if(ktMax(maNhanVien))
            {
                if(ktTrung(maMatHang, maNhanVien))
                {
                    QLBH temp = new QLBH(getMatHang(maMatHang), 
                            getNhanVien(maNhanVien));
                    list.set(row, temp);
                    d.setValueAt(getNhanVien(maNhanVien).getId(), row, 0);
                    d.setValueAt(getNhanVien(maNhanVien).getTen(), row, 1);
                    d.setValueAt(getMatHang(maMatHang).getId(), row, 2);
                    d.setValueAt(getMatHang(maMatHang).getTen(), row, 3);
                    d.setValueAt(getMatHang(maMatHang).getNhom(), row, 4);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Mat hang va nhan vien"
                            + " da ton tai!");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Qua so luong hang nhan vien"
                        + " co the ban!");
            }
        }
        buttonState(true);
    }//GEN-LAST:event_btCapNhatActionPerformed

