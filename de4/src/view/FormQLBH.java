    private void loadMa()
    {
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        try {
            list1 = IOFile.doc(file1);
            list2 = IOFile.doc(file2);
            for(MatHang i : list1)
            {
                jComboBox1.addItem(i.getId() + "");
            }
            for(NhanVien i : list2)
            {
                jComboBox2.addItem(i.getId() + "");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormQLBH.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }
    
    private boolean ktTrung(int ma1, int ma2)
    {
        for(QLBH i : list)
        {
            if(i.getM().getId() == ma1 && i.getN().getId() == ma2)
                return false;
        }
        return true;
    }
    
    private boolean ktMax(int ma)
    {
        int count = 0;
        for(QLBH i : list)
        {
            if(i.getN().getId() == ma)
                count++;
        }
        return count < 6;
    }
    private void loadData()
    {
        try {
            list1 = IOFile.doc(file1);
            list2 = IOFile.doc(file2);
            File f = new File(file);
            if(f.exists())
                list = IOFile.doc(file);
            else
                IOFile.viet(file, list);
            d.setRowCount(0);
            for(QLBH i : list)
            {
                d.addRow(i.toObjects());
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormQLBH.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private MatHang getMatHang(int ma)
    {
        try {
            list1 = IOFile.doc(file1);
            list2 = IOFile.doc(file2);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormQLBH.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        for(MatHang i : list1)
        {
            if(i.getId() == ma)
                return i;
        }
        return null;
    }
    
    private NhanVien getNhanVien(int ma)
    {
        
        for(NhanVien i : list2)
        {
            if(i.getId() == ma)
                return i;
        }
        return null;
    }
