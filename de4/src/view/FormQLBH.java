    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        if(jComboBox3.getSelectedIndex() == 0)
        {
            list.sort(new Comparator<QLBH>(){
                @Override
                public int compare(QLBH o1, QLBH o2) {
                    String t1 = o1.getN().getTen();
                    String t2 = o2.getN().getTen();
                    String name1 = t1.substring(t1.lastIndexOf(" ") + 1) + 1;
                    String name2 = t2.substring(t2.lastIndexOf(" ") + 1) + 1;
                    return name1.compareToIgnoreCase(name2);
                }
                
            });
            d.setRowCount(0);
            for(QLBH i : list)
                d.addRow(i.toObjects());
        }
        else
        {
            list.sort(new Comparator<QLBH>(){
                @Override
                public int compare(QLBH o1, QLBH o2) {
                    String t1 = o1.getM().getNhom();
                    String t2 = o2.getM().getNhom();
                    String name1 = t1.substring(t1.lastIndexOf(" ") + 1) + 1;
                    String name2 = t2.substring(t2.lastIndexOf(" ") + 1) + 1;
                    return name1.compareToIgnoreCase(name2);
                }
                
            });
            d.setRowCount(0);
            for(QLBH i : list)
                d.addRow(i.toObjects());
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed


